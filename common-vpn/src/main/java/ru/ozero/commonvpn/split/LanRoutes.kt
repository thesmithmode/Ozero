        Cidr("11.0.0.0", 8),
        Cidr("12.0.0.0", 6),
        Cidr("16.0.0.0", 4),
        Cidr("32.0.0.0", 3),
        Cidr("64.0.0.0", 3),
        Cidr("96.0.0.0", 6),
        // 100.64.0.0/10 (CGNAT) skipped
        Cidr("100.128.0.0", 9),
        Cidr("101.0.0.0", 8),
        Cidr("102.0.0.0", 7),
