     * 127.0.0.0/8         (loopback),
     * 169.254.0.0/16      (link-local),
     * 172.16.0.0/12       (RFC1918),
     * 192.168.0.0/16      (RFC1918),
     * 224.0.0.0/4         (multicast + reserved 240.0.0.0/4 объединяется с multicast→ 224.0.0.0/3),
     */
