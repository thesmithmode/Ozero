        // 169.254.0.0/16 skipped
        Cidr("169.255.0.0", 16),
        Cidr("170.0.0.0", 7),
        Cidr("172.0.0.0", 12),
        // 172.16.0.0/12 skipped
        Cidr("172.32.0.0", 11),
