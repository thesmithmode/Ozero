package ru.ozero.commonvpn.split

/**
 * Генерация IPv4-routes для BYPASS_LAN: всё пространство `0.0.0.0/0` минус
 * приватные/служебные диапазоны (RFC1918, RFC6598 CGNAT, link-local, loopback,
 * multicast, reserved). Полученный набор маршрутов VpnService.Builder.addRoute()
 * направляет в TUN, а исключённые диапазоны идут через системный default route.
 *
 * Реализация: жёстко зашитая разница между `0.0.0.0/0` и набором приватных CIDR.
 * Алгоритм: для каждой пары соседних диапазонов выводится список CIDR-блоков,
 * покрывающих "дырку". Используем pre-computed список (нет смысла пересчитывать
 * на каждом запуске VPN).
 */
object LanRoutes {

    /**
     * Routes покрывают всё IPv4 кроме:
     * 0.0.0.0/8           (this network),
     * 10.0.0.0/8          (RFC1918),
